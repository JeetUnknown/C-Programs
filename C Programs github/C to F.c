//Centigrade to Farenheit conversion
#include<stdio.h>
int main()
{
	float c,f;
	printf("Enter temparature in Centigrade :");
	scanf("%f",&c);
	f=(c*9+160)/5;
	printf("Temparature equivalent in Farenheit :%f",f);
}



